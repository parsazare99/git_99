package domain;

public class Tag {
    private int tagId;
    private String title;

    public int getId() {
        return tagId;
    }

    public void setId(int id) {
        this.tagId = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }



}
